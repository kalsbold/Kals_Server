	//삭제 쿼리
	bool DeleteQuery(string ID);

private:
	const char * DB_HOST; //DB 호스트
	const char * DB_NAME; //DB 이름.
	const char * DB_SOCK; //DB 소켓
	const char * DB_USER; //DB 사용자
	const char * DB_PASS; //DB 비밀번호

	enum {
	DB_PORT = 1000,	//DB 포트
	DB_OPT = 0		//DB 옵션
	};


